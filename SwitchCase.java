public class SwitchCase{
    public static void main(String[] args){
        int x = 55;
        switch(x){
            case 1: 
            System.out.println("it is the number 1");
            break;
            case 55: 
            System.out.println("it is the number 55");
            break;
            default:
            System.out.println("it is the nummber nothing");
            
        }
    }
}